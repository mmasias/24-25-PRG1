public class Arrays {
    public static void main(String[] args) {
        String[] carritos = new String[3];
        carritos[0] = "Toyota";
        carritos[1] = "Mercedes";
        carritos[2] = "McClaren";
        

        for(int i=0; i<carritos.length; i++){
            System.out.println(carritos[i]);
        }
        
    }
    
}
